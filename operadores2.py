#OPERADORES Boolenaos 
print("operadores booleanos")

# < <=
print(10<10)
print(10<=10)
# > >=
print(10>10)
print(10>=10)
# ==
print(10==10)
# !=
print(10 !=10)


#operadores logicos
print("operadores logicos")
# and(true if all is true) 
print(True and True)
print(True and False)
# or (true if at least one true)
print(False or False)
print(False or True)
#not true
print(not True)
print(not False)


